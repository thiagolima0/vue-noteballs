<template>
  <div class="card has-background-success-dark p-4 mb-5">
    <div class="field">
      <div class="control">
        <textarea
          v-model="modelValue"
          @input="$emit('update:modelValue', modelValue)"
          ref="newNoteRef"
          class="textarea"
          placeholder="new note"
        ></textarea>
      </div>
    </div>
    <div class="field is-grouped is-grouped-right">
      <div class="control">
        <slot name="buttons" />
      </div>
    </div>
  </div>
</template>

<script setup>
import { ref } from "vue";

const props = defineProps({
  modelValue: String,
});

const newNoteRef = ref(null);

const focusTextArea = () => {
  newNoteRef.value.focus();
};

defineExpose({
  focusTextArea,
});
</script>
